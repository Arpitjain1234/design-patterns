public class Location {
    String address;
    String locality;
    String city;
    String state;
    String pincode;

    public Location(String address,String locality,String city, String state, String pincode){
        this.address = address;
        this.locality = locality;
        this.city = city;
        this.state = state;
        this.pincode = pincode;
    }
}
